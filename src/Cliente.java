
public class Cliente {
	private double llegadaSistema, llegadaServidor, salidaSistema;
	
	public Cliente(double llegadaSistema) {
		super();
		this.llegadaSistema = llegadaSistema;
	}

	public double getLlegadaSistema() {
		return llegadaSistema;
	}

	public void setLlegadaSistema(double llegadaSistema) {
		this.llegadaSistema = llegadaSistema;
	}

	public double getLlegadaServidor() {
		return llegadaServidor;
	}

	public void setLlegadaServidor(double llegadaServidor) {
		this.llegadaServidor = llegadaServidor;
	}

	public double getSalidaSistema() {
		return salidaSistema;
	}

	public void setSalidaSistema(double salidaSistema) {
		this.salidaSistema = salidaSistema;
	}
	
	

	
}
